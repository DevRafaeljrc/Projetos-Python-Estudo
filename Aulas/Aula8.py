# Operadores de Comparação:

# ==  Igual

# !=  Diferente

# >   Maior que

# <   Menor que

# >=  Maior ou igual

# <=  Menor ou igual

# Exemplo de comparação:
a = 10
b = 20
c = 10
d = 30

print(a == b)  # False
print(a != b)  # True
print(a > b)   # False
print(a < b)   # True
print(a >= b)  # False
print(a <= b)  # True
print(a == c)  # True
print(a != c)  # False
print(a > c)   # False
print(a < c)   # False
print(a >= c)  # True
print(a <= c)  # True
print(a == d)  # False
print(a != d)  # True
print(a > d)   # False
print(a < d)   # True
print(a >= d)  # False
print(a <= d)  # True

# Exemplo de comparação com strings:

str1 = "banana"
str2 = "maçã"
str3 = "banana"
str4 = "laranja"

print(str1 == str2)  # False
print(str1 != str2)  # True
print(str1 > str2)   # True (banana é maior que maçã)
print(str1 < str2)   # False
print(str1 >= str2)  # True
print(str1 <= str2)  # False
print(str1 == str3)  # True
print(str1 != str3)  # False
print(str1 > str3)   # False
print(str1 < str3)   # False    
print(str1 >= str3)  # True
print(str1 <= str3)  # True
print(str1 == str4)  # False
print(str1 != str4)  # True
print(str1 > str4)   # False
print(str1 < str4)   # True (banana é menor que laranja)
print(str1 >= str4)  # False
print(str1 <= str4)  # True (banana é menor que laranja)






