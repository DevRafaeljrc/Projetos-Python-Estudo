# Modo interativo

# 1. Crie uma lista com os números de 1 a 10 e imprima a lista.
numeros = list(range(1, 11))
print(numeros)

# 2. Crie uma lista com os números de 1 a 10 e imprima o terceiro elemento da lista.
numeros = list(range(1, 11))
print(numeros[2])  # O terceiro elemento tem índice 2

# 3. Crie uma lista com os números de 1 a 10 e imprima o último elemento da lista.
numeros = list(range(1, 11))
print(numeros[-1])  # O último elemento tem índice -1

# 4. Crie uma lista com os números de 1 a 10 e imprima os elementos de índice par.
numeros = list(range(1, 11))
print(numeros[::2])  # Elementos de índice par

# 5. Crie uma lista com os números de 1 a 10 e imprima os elementos de índice ímpar.
numeros = list(range(1, 11))
print(numeros[1::2])  # Elementos de índice ímpar

