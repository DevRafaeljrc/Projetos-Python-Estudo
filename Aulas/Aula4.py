# Constantes e sua convenção:

# - Constantes são variáveis que não devem ser alteradas durante a execução do programa.
# - Em Python, não há uma maneira de declarar constantes, mas é uma convenção usar letras maiúsculas para nomes de constantes.
# - Por exemplo, `PI = 3.14` é uma constante que representa o valor de pi.
# - É uma convenção usar letras maiúsculas para nomes de constantes.

