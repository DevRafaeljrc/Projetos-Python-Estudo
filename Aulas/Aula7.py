# Operadores Aritméticos

# Soma
a = 10 + 5
print(a)  # 15

# Subtração
b = 10 - 5
print(b)  # 5

# Multiplicação
c = 10 * 5
print(c)  # 50

# Divisão
d = 10 / 5
print(d)  # 2.0 

# Divisão inteira
e = 10 // 3
print(e)  # 3

# Módulo (resto da divisão)
f = 10 % 3  
print(f)  # 1

# Potência
g = 10 ** 3
print(g)  # 1000

# Operadores de Atribuição

# Atribuição simples
x = 10
print(x)  # 10

# Atribuição com adição
x += 5  # x = x + 5
print(x)  # 15

# Atribuição com subtração
x -= 3  # x = x - 3
print(x)  # 12

# Atribuição com multiplicação
x *= 2  # x = x * 2
print(x)  # 24

# Atribuição com divisão
x /= 4  # x = x / 4
print(x)  # 6.0

